function cargoProfesores(k) {
  data[k].profesores.forEach((profesor) => {
    document.getElementById("ulProfesores").innerHTML +=
      "<li><a onclick='seleccionoProfe(this)' >" +
      profesor.Apelido +
      ", " +
      profesor.nombre +
      "</a></li>";
  });
}

